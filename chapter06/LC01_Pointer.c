//指针

//指针是什么？
//	在计算机科学中，指针（Pointer）是编程语言中的一个对象，利用地址，它的值直接指向 （points to）存在电脑存储器中另一个地方的值。
//	由于通过地址能找到所需的变量单元，可以 说，地址指向该变量单元。因此，将地址形象化的称为“指针”。意思是通过它能找到以它为地址 的内存单元。

//指针是个变量，存放内存单元的地址（编号)。

#include <stdio.h>

int main()
{
		int a = 10;//在内存中开辟一块空间
		int* p = &a;//这里我们对变量a，取出它的地址，可以使用&操作符。
					//将a的地址存放在p变量中，p就是一个之指针变量。

	return 0;
}