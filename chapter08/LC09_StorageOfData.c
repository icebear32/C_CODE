//C语言数据类型

//练习2 - 代码输出的是什么？

#include <stdio.h>

int main()
{
	char a = -128;
	//10000000000000000000000010000000 - 原码
	//11111111111111111111111101111111 - 反码
	//11111111111111111111111110000000 - 补码
	//10000000
	//%u打印 - char类型整型提升
	// 
	//11111111111111111111111110000000 - 补码
	//无符号打印，原反补相同
	// 
	//因为是%u打印，所以将补码当成无符号数字，而这个数就会很大
	printf("%u\n", a);

	//%d - 打印十进制的有符号数字
	//%u - 打印十进制的无符号数字

	return 0;
}