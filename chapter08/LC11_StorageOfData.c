#include <stdio.h>

/*
	按照补码的形式进行运算，最后格式化成为有符号整数

	-20
	10000000 00000000 00000000 00010100
	11111111 11111111 11111111 11101011
	11111111 11111111 11111111 11101100 - 补码

	10 原码反码补码相同
	00000000 00000000 00000000 00001010 - 补码

	-20 + 10
	11111111 11111111 11111111 11101100 -20	- 补码
	00000000 00000000 00000000 00001010  10 - 补码
	11111111 11111111 11111111 11110110 - 结果 补码
	11111111 11111111 11111111 11110101 - 反码 补码减一得反码
	10000000 00000000 00000000 00001010 - 原码 反码相反得原码 -10 最后格式化成为有符号整数

*/
int main()
{
	int i = -20;
	unsigned int j = 10;
	printf("%d\n", i + j);

	return 0;
}