//整型提升


#include <stdio.h>

int main()
{
	//负数的整形提升
	char c1 = -1;
	//变量c1的二进制位(补码)中只有8个比特位:1111111
	//因为char 为有符号的char
	//所以整形提升的时候，高位补充符号位，即为1
	//提升之后的结果是 :
	//11111111111111111111111111111111
	
	//正数的整形提升
	char c2 = 1;
	////变量c2的二进制位(补码)中只有8个比特位:00000001
	//因为char为有符号的char
	//所以整形提升的时候，高位补充符号位，即为0
	//提升之后的结果是 :
	//00000000000000000000000000000001
	
	//无符号整形提升，高位补O

	return 0;
}