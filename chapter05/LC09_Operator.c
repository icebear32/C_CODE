//位操作符

#include <stdio.h>

int main()
{
	// ^ 按2进制位异或
	int a = 3;
	int b = 5;
	int c = a ^ b;
	//
	//相同为0，相异为1
	//a = 3 - 00000000000000000000000000000011
	//b = 5 - 00000000000000000000000000000101
	//c = 6 - 00000000000000000000000000000110
	//
	printf("%d\n",c);//6

	return 0;
}