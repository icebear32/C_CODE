//按位取反操作

#include <stdio.h>

int main()
{
	//按位取反操作
	/*
	* 按位取反操作，得到负数-1
		对负数进行打印:
		由补码--》反码--》原码打印出来的是原码
	*/
	int a = 0;
	//00000000000000000000000000000000
	//11111111111111111111111111111111 - 补码
	//11111111111111111111111111111110 - 反码
	//10000000000000000000000000000001 - 原码
	//-1
	printf("%d\n", ~a);
	
	return 0;
}