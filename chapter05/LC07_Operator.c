//位操作符

#include <stdio.h>

int main()
{
	// & - 按2进制位与
	int a = 3;
	int b = 5; 
	int c = a & b;
	//
	//两个进行比较，相反为0，相同为1
	//a = 3 - 00000000000000000000000000000011
	//b = 5 - 00000000000000000000000000000101
	//两个进行比较得到
	//c = 1 - 00000000000000000000000000000001
	//
	printf("%d\n", c);//1

	return 0;
}